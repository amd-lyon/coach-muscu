/**
 * Construit une fiche exercice à partir d'un descriptif compact.
 * Les consignes par défaut restent génériques mais utiles ; le profil de
 * risque (clé de l'adaptation aux douleurs) est renseigné explicitement.
 */
type ExerciseSeed = Omit<Partial<Exercise>, 'risk'> &
  Pick<Exercise, 'id' | 'nameFr' | 'primaryMuscle' | 'category'> & {
    /** Seuls les axes non nuls sont renseignés ; le reste vaut 0. */
    risk?: Partial<RiskProfile>
  }

function mk(p: ExerciseSeed): Exercise {
  const risk: RiskProfile = { ...ZERO_RISK, ...(p.risk ?? {}) }
  return {
    nameEn: p.nameEn ?? p.nameFr,
    secondaryMuscles: p.secondaryMuscles ?? [],
    equipment: p.equipment ?? ['machine-guidee'],
    difficulty: p.difficulty ?? 2,
    movementType: p.movementType ?? 'polyarticulaire',
    startPosition: p.startPosition ?? 'Installe-toi de façon stable, dos gainé, regard neutre.',
    execution: p.execution ?? 'Réalise le mouvement de façon contrôlée sur toute l’amplitude utile.',
    breathing: p.breathing ?? 'Expire à l’effort, inspire au retour.',
    rangeOfMotion: p.rangeOfMotion ?? 'Amplitude complète mais sans douleur.',
    commonMistakes: p.commonMistakes ?? ['À-coups', 'Amplitude écourtée', 'Charge trop lourde'],
    safetyTips: p.safetyTips ?? ['Garde le dos gainé', 'Contrôle la descente'],
    variantIds: p.variantIds ?? [],
    alternativeIds: p.alternativeIds ?? [],
    contraindications: p.contraindications ?? [],
    custom: false,
    ...p,
    risk,
  }
}

