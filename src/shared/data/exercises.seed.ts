/** Construit une ImageSource Commons à partir d'un nom de fichier complet. */
function everkineticImage(filename: string) {
  return {
    url: `https://commons.wikimedia.org/wiki/Special:FilePath/${filename}`,
    source: `https://commons.wikimedia.org/wiki/File:${filename}`,
    ...EVERKINETIC_CREDIT,
  }
}

// Applique les illustrations sans écraser celles définies à la main (bench press)
// ni les fiches personnalisées par l'utilisateur.
for (const ex of SEED_EXERCISES) {
  if (ex.machineImage) continue
  let start: string | undefined
  let movement: string | undefined
  if (EVERKINETIC[ex.id]) {
    start = `${EVERKINETIC[ex.id]}-1.png`
    movement = `${EVERKINETIC[ex.id]}-2.png`
  } else if (EVERKINETIC_GIF[ex.id]) {
    start = `${EVERKINETIC_GIF[ex.id]}-1.gif`
    movement = `${EVERKINETIC_GIF[ex.id]}-2.gif`
  } else if (EVERKINETIC_FILES[ex.id]) {
    start = EVERKINETIC_FILES[ex.id].start
    movement = EVERKINETIC_FILES[ex.id].movement
  }
  if (!start || !movement) continue
  ex.machineImage = everkineticImage(start)
  ex.movementImage = everkineticImage(movement)
}
